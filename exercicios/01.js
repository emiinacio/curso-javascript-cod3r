/*
Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
multiplicação e divisão desses valores.
*/

function soma(x, y) {
    return x + y
}
console.log(soma(3, 2))

function sub(x, y) {
    return x - y
}
console.log(sub(3, 2))

function mult(x, y) {
    return x * y
}
console.log(mult(3, 2))